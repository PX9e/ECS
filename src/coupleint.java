
public class coupleint {
String a;
String b;
coupleint(String pa,String pb)
{
	a = pa;
	b = pb;
}
public String getA() {
	return a;
}
public void setA(String a) {
	this.a = a;
}
public String getB() {
	return b;
}
public void setB(String b) {
	this.b = b;
}
public String toString(){
	return a + " - " + b;
	
}


}
